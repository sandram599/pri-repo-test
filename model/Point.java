package model;

import controller.ShapeBuilder;

public class Point {
	
	public int x;
	public int y;
	
	public Point (int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public int setX() {
		return x;
	}
	
	public int setY() {
		return y;
	}
}
